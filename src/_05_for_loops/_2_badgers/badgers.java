package _05_for_loops._2_badgers;

public class badgers {
	public static void main(String[] args) {
		for (int i = 0; i < 2; i++) {
			System.out.println();
			for (int j = 0; j < 12 ; j++) {
			System.out.print("Badger, ");
			}
			for (int k = 0; k < 2; k++) {
			System.out.print("Mushroom, ");
			}
		}
		System.out.println();
		System.out.println("A snake!!");
	}

}
